/* Faca um programa que imprima na tela o seu nome n vezes.
 * O seu nome deve ser passado como parametro para o programa,
 * juntamente com o valor de n. */
public class Ex2 {

	public static void main(String[] args) {
		String nome = new String("Geraldo");
		int n = 100;
		for(int i = 0; i < n; i++) {
			System.out.println(nome);
		}

	}

}
